// End-to-end signet harness for §5.7.6 atomic-intent on-chain recovery.
//
// Phases:
//   1. Verify both wallets are funded (faucet-fed) and seller has an asset
//   2. If seller has no asset: CETCH a small test asset (1000 base units)
//   3. Seller publishAxferIntent (broadcasts commit; posts intent body to worker)
//   4. Taker claimAxferIntent (reserves the intent for 5 min)
//   5. Seller fulfilAxferIntent (posts partial reveal with OP_RETURN to worker)
//   6. Taker fetchAxferFulfilment + takeAxferIntent (broadcasts the reveal)
//   7. Wait for reveal to confirm
//   8. Independently re-scan taker holdings → verify amount + blinding recovered
//      from chain alone via the new OP_RETURN path (no worker fetch needed)
//
// State file at .local/axintent-signet-state.json is resumable.
//
// Run: `node axintent-onchain-e2e-signet.mjs`
//
// Requires:
//   /Users/z/tacit/.local/axintent-signet-test-wallets.json (generated by
//   the wallet-generator; SELLER + TAKER privs, addresses)

import { JSDOM } from 'jsdom';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dom = new JSDOM('<!doctype html><html><body></body></html>', { url: 'http://localhost/' });
globalThis.window = dom.window;
globalThis.document = dom.window.document;
globalThis.localStorage = dom.window.localStorage;
globalThis.location = dom.window.location;
globalThis.navigator = dom.window.navigator;
globalThis.prompt = () => null;
globalThis.alert = () => {};
globalThis.confirm = () => false;
globalThis.__TACIT_NO_INIT__ = true;
globalThis.localStorage.setItem('tacit-network-v1', 'signet');

import * as secp from '@noble/secp256k1';
import { hexToBytes, bytesToHex } from '@noble/hashes/utils';

const dapp = await import('../dapp/tacit.js');

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const STATE_DIR = path.join(__dirname, '..', '.local');
const STATE_FILE = path.join(STATE_DIR, 'axintent-signet-state.json');
const WALLET_FILE = path.join(STATE_DIR, 'axintent-signet-test-wallets.json');

function loadState() { try { return JSON.parse(readFileSync(STATE_FILE, 'utf8')); } catch { return {}; } }
function saveState(s) { if (!existsSync(STATE_DIR)) mkdirSync(STATE_DIR, { recursive: true }); writeFileSync(STATE_FILE, JSON.stringify(s, null, 2)); }
function fail(msg) { console.error(`\n✗ ${msg}\n`); process.exit(1); }
function info(msg) { console.log(`  ${msg}`); }
function ok(msg)   { console.log(`  ✓ ${msg}`); }
function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

if (!existsSync(WALLET_FILE)) {
  fail(`Wallet file missing: ${WALLET_FILE}. Run the wallet generator first.`);
}
const wallets = JSON.parse(readFileSync(WALLET_FILE, 'utf8'));
const SELLER_SK = hexToBytes(wallets.seller.priv_hex);
const TAKER_SK  = hexToBytes(wallets.taker.priv_hex);
const SELLER_PUB = secp.getPublicKey(SELLER_SK, true);
const TAKER_PUB  = secp.getPublicKey(TAKER_SK, true);

function setWallet(sk, pub) {
  dapp.wallet.priv = sk;
  dapp.wallet.pub = pub;
  dapp.invalidateHoldingsCache();
}

setWallet(SELLER_SK, SELLER_PUB);
const SELLER_ADDR = dapp.wallet.address();
setWallet(TAKER_SK, TAKER_PUB);
const TAKER_ADDR = dapp.wallet.address();

console.log('=== axintent-onchain-e2e-signet ===');
console.log(`  seller: ${SELLER_ADDR}`);
console.log(`  taker:  ${TAKER_ADDR}`);
console.log(`  state:  ${STATE_FILE}\n`);

const state = loadState();

// ---- Phase 1: verify funding ----
console.log('[1/8] Verify funding');
async function balance(addr) {
  const res = await fetch(`https://mempool.space/signet/api/address/${addr}`);
  if (!res.ok) throw new Error(`addr fetch ${res.status}`);
  const j = await res.json();
  const c = j.chain_stats || {};
  const m = j.mempool_stats || {};
  return (c.funded_txo_sum || 0) - (c.spent_txo_sum || 0) + (m.funded_txo_sum || 0) - (m.spent_txo_sum || 0);
}
const sb = await balance(SELLER_ADDR);
const tb = await balance(TAKER_ADDR);
info(`seller: ${sb} sats`);
info(`taker:  ${tb} sats`);
if (sb < 10_000) fail(`seller needs ≥10k sats (has ${sb})`);
if (tb < 50_000) fail(`taker needs ≥50k sats (has ${tb})`);
ok('both wallets funded');

// ---- Phase 2: ensure seller has an asset to list ----
console.log('\n[2/8] Seller asset holdings');
setWallet(SELLER_SK, SELLER_PUB);
let holdings = await dapp.scanHoldings(true);
let targetAssetId = null;
let targetUtxo = null;
let targetAsset = null;
for (const [aid, h] of holdings) {
  const u = h.utxos.find(x => x.utxo.value >= 546);
  if (u) { targetAssetId = aid; targetUtxo = u; targetAsset = h; break; }
}
if (!targetUtxo) {
  if (state.etch?.reveal_txid) {
    info(`(resuming) prior etch reveal ${state.etch.reveal_txid.slice(0,16)}… — waiting for confirm + re-scan`);
    const etchDeadline = Date.now() + 30 * 60 * 1000;
    while (Date.now() < etchDeadline && !targetUtxo) {
      const s = await fetch(`https://mempool.space/signet/api/tx/${state.etch.reveal_txid}/status`).then(r => r.json()).catch(() => null);
      if (s?.confirmed) {
        ok(`etch reveal confirmed at block ${s.block_height}`);
        dapp.invalidateHoldingsCache();
        holdings = await dapp.scanHoldings(true);
        for (const [aid, h] of holdings) {
          const u = h.utxos.find(x => x.utxo.value >= 546);
          if (u) { targetAssetId = aid; targetUtxo = u; targetAsset = h; break; }
        }
        break;
      }
      info(`  awaiting etch confirmation… (in mempool? ${s ? 'yes' : 'not visible yet'})`);
      await sleep(30_000);
    }
  }
}
if (!targetUtxo && state.etch?.reveal_txid) {
  fail(`prior etch reveal ${state.etch.reveal_txid} did not confirm within timeout, or its asset isn't appearing in holdings. ` +
       `Check the txid on https://mempool.space/signet/tx/${state.etch.reveal_txid} and re-run, or clear state.etch from ${STATE_FILE} to allow a fresh etch.`);
}
if (!targetUtxo) {
  info('seller has no asset — etching 1000 TESTAMM (decimals=0)');
  const r = await dapp.buildAndBroadcastCEtch({
    ticker: 'TESTAMM', supplyBase: 1000n, decimals: 0,
    imageUri: null, mintable: false,
    onProgress: (s) => info(`  [etch] ${s}`),
  });
  // buildAndBroadcastCEtch returns { commitTxid, revealTxid, assetIdHex, ... }
  state.etch = { commit_txid: r.commitTxid, reveal_txid: r.revealTxid, asset_id: r.assetIdHex };
  saveState(state);
  ok(`etch broadcast: reveal=${r.revealTxid.slice(0,16)}…`);
  info('waiting up to 30 min for confirm + holdings refresh…');
  const deadline = Date.now() + 30 * 60 * 1000;
  while (Date.now() < deadline) {
    await sleep(30_000);
    const s = await fetch(`https://mempool.space/signet/api/tx/${r.revealTxid}/status`).then(x => x.json()).catch(() => null);
    if (s?.confirmed) { ok(`etch reveal confirmed at block ${s.block_height}`); break; }
    info(`  awaiting etch confirmation…`);
  }
  dapp.invalidateHoldingsCache();
  holdings = await dapp.scanHoldings(true);
  for (const [aid, h] of holdings) {
    const u = h.utxos.find(x => x.utxo.value >= 546);
    if (u) { targetAssetId = aid; targetUtxo = u; targetAsset = h; break; }
  }
  if (!targetUtxo) fail('still no asset after etch confirm — investigate');
}
info(`asset: ${targetAssetId.slice(0,16)}… (${targetAsset.ticker})`);
info(`utxo:  ${targetUtxo.utxo.txid.slice(0,16)}…:${targetUtxo.utxo.vout} (amount=${targetUtxo.amount})`);
ok('seller has spendable asset UTXO');

// ---- Phase 3: publish atomic intent ----
console.log('\n[3/8] Seller: publish atomic intent');
const PRICE_SATS = 20_000;
const EXPIRY = Math.floor(Date.now() / 1000) + 24 * 3600;
const SECRETS_KEY = `tacit-axintent-secrets-v1:signet`;
const PENDING_KEY = `tacit-axintent-pending-v1:signet`;
if (state.intent?.intent_id && state.intent.secrets_blob) {
  info(`(resuming) intent ${state.intent.intent_id.slice(0,16)}…`);
  // Restore localStorage state captured at first publish so fulfilAxferIntent
  // can find the locally-cached `r` and pending body (jsdom localStorage is
  // in-memory and doesn't survive harness restart).
  globalThis.localStorage.setItem(SECRETS_KEY, state.intent.secrets_blob);
  if (state.intent.pending_blob) globalThis.localStorage.setItem(PENDING_KEY, state.intent.pending_blob);
} else if (state.intent?.intent_id && !state.intent.secrets_blob) {
  info(`(broken resume) intent ${state.intent.intent_id.slice(0,16)}… exists on worker but local secret was lost — cancelling and republishing`);
  setWallet(SELLER_SK, SELLER_PUB);
  try {
    await dapp.cancelAxferIntent({ assetIdHex: state.intent.asset_id, intentIdHex: state.intent.intent_id });
    ok('cancelled broken intent');
  } catch (e) {
    info(`(cancel failed — intent may already be gone: ${e.message})`);
  }
  delete state.intent;
  delete state.claim;
  delete state.fulfil;
  delete state.take;
  saveState(state);
  setWallet(SELLER_SK, SELLER_PUB);
}
if (!state.intent?.intent_id) {
  try {
    const r = await dapp.publishAxferIntent({
      utxoTxid: targetUtxo.utxo.txid,
      utxoVout: targetUtxo.utxo.vout,
      priceSats: PRICE_SATS,
      expiry: EXPIRY,
      onProgress: (s) => info(`  [pub] ${s}`),
    });
    state.intent = {
      intent_id: r.intent_id,
      asset_id: r.asset_id,
      price_sats: PRICE_SATS,
      expiry: EXPIRY,
      // Stash the localStorage entries the dapp wrote at publish time. We
      // re-inject these on resume so the (jsdom-only, ephemeral) localStorage
      // is repopulated before fulfilAxferIntent runs.
      secrets_blob: globalThis.localStorage.getItem(SECRETS_KEY) || '',
      pending_blob: globalThis.localStorage.getItem(PENDING_KEY) || '',
    };
    saveState(state);
    ok(`intent published: ${r.intent_id.slice(0,16)}…`);
  } catch (e) {
    // publishAxferIntent broadcasts the commit BEFORE attempting the worker
    // POST. If the POST fails (e.g. worker chain-API 522), the commit is on
    // chain but the worker doesn't know about it — yet localStorage holds
    // the pending body + the `r` secret. Capture both into state so the
    // next run can retry just the POST step via dapp.postAxferIntentBody.
    const secretsBlob = globalThis.localStorage.getItem(SECRETS_KEY) || '';
    const pendingBlob = globalThis.localStorage.getItem(PENDING_KEY) || '';
    if (secretsBlob && pendingBlob) {
      const secrets = JSON.parse(secretsBlob);
      const pendings = JSON.parse(pendingBlob);
      const intentId = Object.keys(pendings)[0];
      const pendingEntry = pendings[intentId];
      if (intentId && pendingEntry?.body && pendingEntry?.asset_id) {
        state.intent = {
          intent_id: intentId,
          asset_id: pendingEntry.asset_id,
          price_sats: PRICE_SATS,
          expiry: EXPIRY,
          secrets_blob: secretsBlob,
          pending_blob: pendingBlob,
          partial: true,
          partial_error: e.message,
        };
        saveState(state);
        info(`(captured partial state: commit broadcast but POST failed — resume will retry POST)`);
      }
    }
    throw e;
  }
} else if (state.intent.partial) {
  info(`(resuming) partial publish — commit on chain, retrying worker POST`);
  // Restore localStorage so dapp.postAxferIntentBody finds the pending body.
  globalThis.localStorage.setItem(SECRETS_KEY, state.intent.secrets_blob);
  globalThis.localStorage.setItem(PENDING_KEY, state.intent.pending_blob);
  const pendings = JSON.parse(state.intent.pending_blob);
  const body = pendings[state.intent.intent_id].body;
  await dapp.postAxferIntentBody(state.intent.asset_id, body);
  delete state.intent.partial;
  delete state.intent.partial_error;
  saveState(state);
  ok(`POST retried successfully — intent ${state.intent.intent_id.slice(0,16)}… now visible on worker`);
}

// ---- Phase 4: taker claims ----
console.log('\n[4/8] Taker: claim intent');
setWallet(TAKER_SK, TAKER_PUB);
if (state.claim?.claimed_at) {
  info(`(resuming) claim already placed at ${new Date(state.claim.claimed_at * 1000).toISOString()}`);
} else {
  const c = await dapp.claimAxferIntent({
    assetIdHex: state.intent.asset_id,
    intentIdHex: state.intent.intent_id,
    priceSats: state.intent.price_sats,
  });
  state.claim = { claimed_at: Math.floor(Date.now() / 1000), claim: c.claim || c };
  saveState(state);
  ok('claim accepted by worker');
}

// ---- Phase 5: seller fulfils (this triggers the NEW OP_RETURN emission) ----
console.log('\n[5/8] Seller: fulfil claim (with new OP_RETURN at vout[2])');
setWallet(SELLER_SK, SELLER_PUB);
// Re-fetch the intent from the worker's LIST endpoint and filter to ours.
// The worker has no single-intent GET (DELETE only on that path), so we use
// the list endpoint that the browse UI uses, then pick our intent_id.
const WORKER = 'https://tacit-pin.rosscampbell9.workers.dev';
const listResp = await fetch(`${WORKER}/assets/${state.intent.asset_id}/atomic-intents?network=signet`);
if (!listResp.ok) fail(`could not list intents (HTTP ${listResp.status})`);
const listJson = await listResp.json();
const intent = (listJson.intents || []).find(i => i.intent_id === state.intent.intent_id);
if (!intent) fail(`our intent ${state.intent.intent_id} not in list (worker dropped it?)`);
// The claim was cached in state when we POSTed it — the worker has no
// single-claim GET endpoint, so we use the cached body. claim.taker_pubkey
// is what fulfilAxferIntent reads.
const claim = state.claim?.claim;
if (!claim || !claim.taker_pubkey) fail('cached claim body missing in state — re-run claim');

if (state.fulfil?.posted) {
  info(`(resuming) fulfilment already posted`);
} else {
  const f = await dapp.fulfilAxferIntent({
    assetIdHex: state.intent.asset_id,
    intentIdHex: state.intent.intent_id,
    intent,
    claim,
  });
  state.fulfil = { posted: true, response: f };
  saveState(state);
  ok('fulfilment posted to worker (partial reveal includes OP_RETURN(40) at vout[2])');
}

// ---- Phase 6: taker takes, broadcasts the reveal ----
console.log('\n[6/8] Taker: take + broadcast reveal');
setWallet(TAKER_SK, TAKER_PUB);
if (state.take?.reveal_txid) {
  info(`(resuming) reveal ${state.take.reveal_txid.slice(0,16)}… already broadcast`);
} else {
  const fulfilmentResp = await dapp.fetchAxferFulfilment({
    assetIdHex: state.intent.asset_id,
    intentIdHex: state.intent.intent_id,
  });
  if (!fulfilmentResp) fail('worker has no fulfilment record');
  // Worker wraps response as {ok, fulfilment, intent}; takeAxferIntent
  // expects the inner fulfilment object (which has fulfilment_sig etc.).
  const fulfilment = fulfilmentResp.fulfilment || fulfilmentResp;
  const r = await dapp.takeAxferIntent({
    intent,
    fulfilment,
    onProgress: (s) => info(`  [take] ${s}`),
  });
  state.take = { reveal_txid: r.txid || r.reveal_txid };
  saveState(state);
  ok(`reveal broadcast: ${state.take.reveal_txid}`);
}

// ---- Phase 7: wait for reveal to confirm ----
console.log('\n[7/8] Wait for reveal to confirm (polling every 30s, up to 30 min)');
const deadline = Date.now() + 30 * 60 * 1000;
let revealStatus = null;
while (Date.now() < deadline) {
  revealStatus = await fetch(`https://mempool.space/signet/api/tx/${state.take.reveal_txid}/status`).then(r => r.json()).catch(() => null);
  if (revealStatus?.confirmed) { ok(`reveal confirmed at block ${revealStatus.block_height}`); break; }
  info(`  awaiting confirmation… (mempool? ${revealStatus ? 'yes' : 'not visible yet'})`);
  await sleep(30_000);
}
if (!revealStatus?.confirmed) fail('reveal not confirmed within 30 min');

// ---- Phase 8: independent recovery from chain alone ----
console.log('\n[8/8] Independent recovery: scan taker chain state, verify OP_RETURN path');
info('inspecting reveal tx vouts…');
const revealTx = await fetch(`https://mempool.space/signet/api/tx/${state.take.reveal_txid}`).then(r => r.json());
info(`reveal has ${revealTx.vout.length} vouts:`);
for (let i = 0; i < revealTx.vout.length; i++) {
  const v = revealTx.vout[i];
  info(`  vout[${i}] value=${v.value} scriptpubkey=${v.scriptpubkey.slice(0, 12)}…(${v.scriptpubkey.length / 2}B)`);
}
const opReturnVout = revealTx.vout.find(v => v.scriptpubkey.startsWith('6a28'));
if (!opReturnVout) fail('no OP_RETURN(40) found in reveal — fulfilment did not include the on-chain payload');
ok(`OP_RETURN(40) found at vout (${opReturnVout.scriptpubkey.slice(0, 8)}…)`);

// Now drive scanHoldings as the taker. We want to confirm the wallet picks
// up the asset balance via the OP_RETURN recovery path — i.e., even without
// the worker's enc_recipient_blinding ciphertext, the wallet recovers from
// chain alone. To validate: clear the cached opening, re-scan, assert the
// taker's holdings include the expected amount under the right asset_id.
setWallet(TAKER_SK, TAKER_PUB);
dapp.invalidateHoldingsCache();
const takerHoldings = await dapp.scanHoldings(true);
const tHold = takerHoldings.get(state.intent.asset_id);
if (!tHold) fail(`taker has no holdings for asset ${state.intent.asset_id.slice(0,16)}…`);
const matchingUtxo = tHold.utxos.find(u => u.utxo.txid === state.take.reveal_txid && u.utxo.vout === 0);
if (!matchingUtxo) {
  info(`taker holdings for ${state.intent.asset_id.slice(0,16)}…: balance=${tHold.balance} utxos=${tHold.utxos.length} ghosts=${tHold.ghosts.length}`);
  fail('recovered UTXO not found in taker holdings');
}
info(`taker recovered: amount=${matchingUtxo.amount}, blinding=${matchingUtxo.blinding.toString(16).slice(0,16)}…`);
ok('atomic intent receipt recovered from chain + privkey alone');

console.log('\n========== SUCCESS ==========');
console.log(`  reveal tx: ${state.take.reveal_txid}`);
console.log(`  asset:     ${state.intent.asset_id}`);
console.log(`  amount:    ${matchingUtxo.amount}`);
console.log(`  vouts:     ${revealTx.vout.length} (including OP_RETURN(40) at vout[2])`);
console.log(`  recovery:  via OP_RETURN ECDH decrypt, no worker fetch needed`);
console.log(`\n  Validates: §5.7.6 atomic-intent on-chain recovery e2e.`);
